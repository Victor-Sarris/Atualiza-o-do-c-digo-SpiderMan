// Inicializa o PWM no pino do buzzer
void pwm_init_buzzer(uint pin) {
    gpio_set_function(pin, GPIO_FUNC_PWM);
    uint slice_num = pwm_gpio_to_slice_num(pin);
    pwm_set_enabled(slice_num, true);
}

// Função para tocar uma nota específica no buzzer
void play_note(uint pin, int frequency, int duration_ms) {
    uint slice_num = pwm_gpio_to_slice_num(pin);

    if (frequency == NOTE_REST) {
        pwm_set_gpio_level(pin, 0);  // Silêncio nas pausas
    } else {
        uint32_t clock = clock_get_hz(clk_sys);
        uint32_t wrap = clock / frequency - 1;
        pwm_set_wrap(slice_num, wrap);
        pwm_set_gpio_level(pin, wrap / 2);
    }

    sleep_ms(duration_ms);
    pwm_set_gpio_level(pin, 0);  // Desliga o som após a nota
    sleep_ms(50);  // Pequena pausa entre notas
}

// Função para tocar a melodia do Homem-Aranha
void play_melody() {
    for (int i = 0; i < MELODY_LENGTH; i++) {
        play_note(BUZZER_PIN, spider_man_theme[i].frequency, spider_man_theme[i].duration_ms);
    }
}

